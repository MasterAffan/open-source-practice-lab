def add(a, b):
    # Issue #3: Add docstring
    """Return the sum of two numbers.

    This function takes two numeric values and returns their sum. It works with
    integers, floats, and other types that support the `+` operator.

    Args:
        a: The first number.
        b: The second number.

    Returns:
        The result of adding `a` and `b`.

    Raises:
        TypeError: If `a` and `b` cannot be added using the `+` operator.
    """
    return a + b
